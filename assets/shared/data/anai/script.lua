local yy = 625; 

local xx2 = 500; 
local yy2 = 625; 

local xx3 = 520;
local yy3 = 450;

local ofs = 15;
local followchars = true; 
local del = 0;
local del2 = 0;






function onEvent(n,v1,v2)


	if n == 'AnaiEvents' then

if v1 == 'midl' then

xx = 350
xx2 = 350

end

if v1 == 'up' then

setProperty('health', 1)
			doTweenAlpha('bgfade1', 'healthBar', 1, 1, 'sineInOut')	
				doTweenAlpha('bgfade2', 'iconP1', 1, 1, 'sineInOut')	
					doTweenAlpha('bgfade3', 'iconP2', 1, 1, 'sineInOut')	

setProperty('defaultCamZoom', 0.5)
yy = -1525
yy2 = -1525

doTweenAlpha('ovgon', 'ov', 0, 1, 'sineInOut')
doTweenY('geser', 'gey', -1850, 1, 'sineOut')
doTweenY('bfup', 'boyfriendGroup', -2130, 1.1, 'sineOut')
